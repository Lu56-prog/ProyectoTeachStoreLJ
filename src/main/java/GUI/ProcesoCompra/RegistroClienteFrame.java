    private void btnBuscaCliente(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaCliente
        String documento = txtDocumento.getText();
        for(Cliente cliente:listaClientes){
            if(cliente.getCedula().equals(documento)){
                //Cada que busca un cliente se muestra la info de este en la tabla
                mt.setRowCount(0);
                String encabezado [] = {"Nombre", "Identificacion", "Contacto", "Correo Electronico", "Direccion", "ID"};
                mt.setColumnIdentifiers(encabezado);

                tablaCliente.setModel(mt);

                //DATOS CLIENTE
                String nombre = cliente.getNombre();
                String cedula = cliente.getCedula();
                String telefono = cliente.getTelefono();
                String direccion = cliente.getDireccion();
                String email = cliente.getCorreo();
                String id = cliente.getId();

                mt.addRow(new Object[] {nombre, cedula, telefono, email, direccion, id});
            }
        }
    }//GEN-LAST:event_btnBuscaCliente


