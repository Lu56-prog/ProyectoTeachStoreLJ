    private void btnRegistrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarClienteActionPerformed
        String documento = txtDocumento.getText();
        boolean registroValido = false;
        
        for(Cliente cliente:listaClientes){
            if(cliente.getCedula().equals(documento)){
                registroValido = true;
                JOptionPane.showMessageDialog(null, "BIENVENIDO " + cliente.getNombre(), "REGISTRO CLIENTE EXICTOSO", JOptionPane.HEIGHT);
                //Agregamos al ticket el cliente que va a realizar la venta
                RegistroCajeroFrame.ticket.setCliente(cliente);
                dispose();
                recargarPagina.recargarCrearTicket(); 
            }
        }
        if (registroValido == false){
            JOptionPane.showMessageDialog(null, "REGISTRO NO VALIDO", "CLIENTE NO VALIDO", JOptionPane.ERROR_MESSAGE);
            dispose();
            recargarPagina.recargarRegistrarCajero();
        }
    }//GEN-LAST:event_btnRegistrarClienteActionPerformed

