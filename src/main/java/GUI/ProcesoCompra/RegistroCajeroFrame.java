    private void btnRegistrarCajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarCajeroActionPerformed
        String documento = txtDocumento.getText();
        String codigo =  txtCodigo.getText();
        List<Empleado> listaEmpleados = TeachStoreLJ.usuarios.listaEmpleados;
        boolean registroValido = false;
        
        for (Empleado empleado:listaEmpleados){
            if(empleado.getCargo().equals("Cajero")){
                registroValido = true;
                if(empleado.getCedula().equals(documento)){
                    if(empleado.getId().equals(codigo)){
                       JOptionPane.showMessageDialog(null, "BIENVENIDO " + empleado.getNombre(), "REGISTRO CAJERO EXICTOSO", JOptionPane.HEIGHT);
                        dispose();
                        recargarPagina.recargarRegistrarCliente();
                        //Agregamos al ticket el cajero encargado de la venta
                        ticket.setCajero(empleado);
                        
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "EL CODIGO INGRESADO ES ERRONEO", "CODIGO NO VALIDO", JOptionPane.ERROR_MESSAGE);
                        dispose();
                        recargarPagina.recargarRegistrarCajero();
                    }
                }
            } 
        } 
        if(registroValido == false){
            JOptionPane.showMessageDialog(null, "REGISTRO NO VALIDO", "EMPLEADO NO VALIDO", JOptionPane.ERROR_MESSAGE);
            dispose();
            recargarPagina.recargarRegistrarCajero();
        }
    }//GEN-LAST:event_btnRegistrarCajeroActionPerformed


